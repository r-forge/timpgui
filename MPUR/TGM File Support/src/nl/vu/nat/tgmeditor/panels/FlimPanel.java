    @Override
    public void setValue(JComponent source, Object value) {
        if (source == JRbMirf_True) {
            flimPanelModel.setMirf(true);
        }
        if (source == JRbMirf_False) {
            flimPanelModel.setMirf(false);
        }
        if (source==jRbConvalg_1) {
            if((Boolean)value) flimPanelModel.setConvalg(1);
        }
        if (source==jRbConvalg_2) {
            if((Boolean)value) flimPanelModel.setConvalg(1);
        }
        if (source==jRbConvalg_3) {
            if((Boolean)value) flimPanelModel.setConvalg(1);
        }
        if (source == jTReftauTextField) {
            flimPanelModel.setReftau((Double)value);
        }
        endUIChange();
    }

