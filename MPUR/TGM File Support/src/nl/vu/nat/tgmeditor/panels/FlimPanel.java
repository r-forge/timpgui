    public FlimPanel(SectionView view, TgmDataObject dObj, FlimPanelModel flimPanelModel) {
        super(view);
        this.dObj = dObj;
        this.flimPanelModel = flimPanelModel;
        initComponents();
        if (flimPanelModel.isMirf()) {
            JRbMirf_True.setSelected(true);
        } else if (!flimPanelModel.isMirf()){
            JRbMirf_False.setSelected(true);
            flimPanelModel.setMirf(false); endUIChange();
        } else {
            JRbMirf_False.setSelected(true);
            flimPanelModel.setMirf(false); endUIChange();
        }
        
        switch (flimPanelModel.getConvalg()) {
            case 1:
                jRbConvalg_1.setSelected(true);
                break;
            case 2:
                jRbConvalg_2.setSelected(true);
                break;
            case 3:
                jRbConvalg_3.setSelected(true);
                break;
            default: {
                jRbConvalg_1.setSelected(true);
                flimPanelModel.setConvalg(1); endUIChange();
                break;
            }
        } //end switch
        jTReftauTextField.setText(String.valueOf(flimPanelModel.getReftau()));

        addModifier(jRbConvalg_1);
        addModifier(jRbConvalg_2);
        addModifier(jRbConvalg_3);
        addModifier(jTReftauTextField);
        addModifier(JRbMirf_False);
        addModifier(JRbMirf_True);
    }

